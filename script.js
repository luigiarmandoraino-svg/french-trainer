function makeChoices(lesson, index) {
  const correct = lesson.sentences[index].fr;
  const pool = lesson.sentences.map(s => s.fr).filter(s => s !== correct);
  const wrong = pool.sort(() => Math.random() - .5).slice(0, 2);
  return [correct, ...wrong].sort(() => Math.random() - .5);
}

function blankFrench(sentence) {
  const words = sentence.split(/\s+/);
  const candidates = words.map((w, i) => ({ w, i })).filter(x => x.i > 0 && x.i < words.length - 1 && x.w.length > 3 && !/[?.!,;:]$/.test(x.w));
  const picked = candidates[0] || { w: words[Math.min(1, words.length - 1)], i: Math.min(1, words.length - 1) };
  const blanked = words.map((w, i) => i === picked.i ? "______" : w).join(" ");
  return { blanked, answer: picked.w };
}

