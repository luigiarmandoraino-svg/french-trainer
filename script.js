let currentLessonIndex = Number(localStorage.getItem("currentLessonIndex")) || 0;
let currentExerciseIndex = Number(localStorage.getItem("currentExerciseIndex")) || 0;
let score = Number(localStorage.getItem("score")) || 0;
let attempts = Number(localStorage.getItem("attempts")) || 0;

function normalize(text) {
  return text
    .toLowerCase()
    .trim()
    .replace(/[’']/g, "'")
    .replace(/[?.!,]/g, "")
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "");
}

function speakFrench(text) {
  const utterance = new SpeechSynthesisUtterance(text);
  utterance.lang = "fr-FR";
  utterance.rate = 0.85;
  speechSynthesis.cancel();
  speechSynthesis.speak(utterance);
}

function escapeForButton(text) {
  return text
    .replace(/\\/g, "\\\\")
    .replace(/'/g, "\\'")
    .replace(/"/g, "&quot;");
}

function renderLesson() {
  const lesson = lessons[currentLessonIndex];

  document.getElementById("lessonCounter").textContent = `Lesson ${lesson.number}`;
  document.getElementById("lessonTitle").textContent = lesson.title;
  document.getElementById("lessonWhy").textContent = lesson.why;
  document.getElementById("ruleFrench").textContent = lesson.ruleFrench;
  document.getElementById("ruleEnglish").textContent = lesson.ruleEnglish;
  document.getElementById("pronunciationTip").textContent = lesson.pronunciationTip;
  document.getElementById("commonMistake").textContent = lesson.commonMistake;
  document.getElementById("learningAdvice").textContent = lesson.learningAdvice;
  document.getElementById("nextLesson").textContent = lesson.nextLesson;

  const practiceList = document.getElementById("practiceList");
  practiceList.innerHTML = "";

  lesson.practice.forEach((item) => {
    const div = document.createElement("div");
    div.className = "practice-item";

    div.innerHTML = `
      <p class="english">${item.en}</p>
      <p class="french">${item.fr}</p>
      <button onclick="speakFrench('${escapeForButton(item.fr)}')">Listen</button>
    `;

    practiceList.appendChild(div);
  });

  renderExercise();
  updateScore();
}

function renderExercise() {
  const lesson = lessons[currentLessonIndex];
  const exercise = lesson.exercises[currentExerciseIndex % lesson.exercises.length];

  document.getElementById("exerciseEnglish").textContent = exercise.en;
  document.getElementById("answerInput").value = "";
  document.getElementById("feedback").textContent = "";
  document.getElementById("feedback").className = "";
  document.getElementById("correctAnswerBox").innerHTML = "";
}

function checkAnswer() {
  const lesson = lessons[currentLessonIndex];
  const exercise = lesson.exercises[currentExerciseIndex % lesson.exercises.length];

  const userAnswer = document.getElementById("answerInput").value;
  const correctAnswer = exercise.fr;

  const normalizedUser = normalize(userAnswer);
  const normalizedCorrect = normalize(correctAnswer);

  attempts++;

  const feedback = document.getElementById("feedback");
  const correctAnswerBox = document.getElementById("correctAnswerBox");

  if (normalizedUser === normalizedCorrect) {
    score++;
    feedback.className = "correct";
    feedback.textContent = "Correct! Très bien.";
  } else if (isAlmostCorrect(normalizedUser, normalizedCorrect)) {
    score += 0.5;
    feedback.className = "almost";
    feedback.textContent = "Almost correct. Check accents, word order, or small missing words.";
  } else {
    feedback.className = "wrong";
    feedback.textContent = "Not yet. Compare your answer with the correct sentence.";
  }

  correctAnswerBox.innerHTML = `
    <p><strong>Correct answer:</strong></p>
    <p class="french">${correctAnswer}</p>
    <button onclick="speakFrench('${escapeForButton(correctAnswer)}')">Listen</button>
  `;

  localStorage.setItem("score", score);
  localStorage.setItem("attempts", attempts);

  updateScore();
}

function isAlmostCorrect(user, correct) {
  const userWords = user.split(" ").filter(Boolean);
  const correctWords = correct.split(" ").filter(Boolean);

  let matchingWords = 0;

  userWords.forEach((word) => {
    if (correctWords.includes(word)) {
      matchingWords++;
    }
  });

  return matchingWords >= Math.max(1, correctWords.length - 2);
}

function nextExercise() {
  const lesson = lessons[currentLessonIndex];
  currentExerciseIndex = (currentExerciseIndex + 1) % lesson.exercises.length;

  localStorage.setItem("currentExerciseIndex", currentExerciseIndex);
  renderExercise();
}

function nextLessonButton() {
  if (currentLessonIndex < lessons.length - 1) {
    currentLessonIndex++;
    currentExerciseIndex = 0;

    localStorage.setItem("currentLessonIndex", currentLessonIndex);
    localStorage.setItem("currentExerciseIndex", currentExerciseIndex);

    renderLesson();
  }
}

function previousLesson() {
  if (currentLessonIndex > 0) {
    currentLessonIndex--;
    currentExerciseIndex = 0;

    localStorage.setItem("currentLessonIndex", currentLessonIndex);
    localStorage.setItem("currentExerciseIndex", currentExerciseIndex);

    renderLesson();
  }
}

function updateScore() {
  document.getElementById("scoreDisplay").textContent =
    `Score: ${score} / ${attempts}`;
}

function resetProgress() {
  score = 0;
  attempts = 0;
  currentLessonIndex = 0;
  currentExerciseIndex = 0;

  localStorage.setItem("score", score);
  localStorage.setItem("attempts", attempts);
  localStorage.setItem("currentLessonIndex", currentLessonIndex);
  localStorage.setItem("currentExerciseIndex", currentExerciseIndex);

  renderLesson();
}

