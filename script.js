const state = {
  current: Number(localStorage.getItem("currentLesson") || 1),
  tab: "learn",
  completed: JSON.parse(localStorage.getItem("completedLessons") || "[]"),
  theme: localStorage.getItem("theme") || "light",
};

const $ = (selector, root = document) => root.querySelector(selector);
const $$ = (selector, root = document) => Array.from(root.querySelectorAll(selector));
const tg = window.Telegram && window.Telegram.WebApp ? window.Telegram.WebApp : null;

function initTelegram() {
  if (!tg) return;
  tg.ready();
  tg.expand();
  if (tg.themeParams && tg.themeParams.bg_color) {
    document.documentElement.style.setProperty("--telegram-bg", tg.themeParams.bg_color);
  }
}

function vibrate() {
  try { tg?.HapticFeedback?.selectionChanged?.(); } catch (e) {}
}

function getLesson() {
  return LESSONS.find(l => l.id === state.current) || LESSONS[0];
}

function saveState() {
  localStorage.setItem("currentLesson", String(state.current));
  localStorage.setItem("completedLessons", JSON.stringify(state.completed));
  localStorage.setItem("theme", state.theme);
}

function setTheme(theme) {
  state.theme = theme;
  document.documentElement.setAttribute("data-theme", theme);
  $("#themeToggle").textContent = theme === "dark" ? "🌙" : "☀️";
  saveState();
}

function escapeHtml(value = "") {
  return String(value).replace(/[&<>"]/g, char => ({"&":"&amp;","<":"&lt;",">":"&gt;","\"":"&quot;"}[char] || char));
}

function normalizeAnswer(value = "") {
  return value
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[’']/g, "'")
    .replace(/[^a-z0-9' ]/gi, "")
    .replace(/\s+/g, " ")
    .trim();
}

function speakFrench(text) {
  if (!("speechSynthesis" in window)) {
    showToast("Text-to-speech is not available in this browser.");
    return;
  }
  window.speechSynthesis.cancel();
  const utterance = new SpeechSynthesisUtterance(text);
  utterance.lang = "fr-FR";
  utterance.rate = 0.86;
  const voices = window.speechSynthesis.getVoices();
  const frenchVoice = voices.find(v => v.lang && v.lang.toLowerCase().startsWith("fr"));
  if (frenchVoice) utterance.voice = frenchVoice;
  window.speechSynthesis.speak(utterance);
}

function showToast(message) {
  const old = $(".toast");
  if (old) old.remove();
  const toast = document.createElement("div");
  toast.className = "toast";
  toast.textContent = message;
  document.body.appendChild(toast);
  setTimeout(() => toast.remove(), 2300);
}

function setupSelector() {
  const select = $("#lessonSelect");
  select.innerHTML = LESSONS.map(l => `<option value="${l.id}">Class ${l.id} · ${escapeHtml(l.grammar)}</option>`).join("");
  select.value = String(state.current);
  select.addEventListener("change", e => {
    state.current = Number(e.target.value);
    state.tab = "learn";
    saveState();
    render();
  });
  $("#prevBtn").addEventListener("click", () => moveLesson(-1));
  $("#nextBtn").addEventListener("click", () => moveLesson(1));
  $("#themeToggle").addEventListener("click", () => setTheme(state.theme === "dark" ? "light" : "dark"));
}

function moveLesson(delta) {
  const next = Math.min(LESSONS.length, Math.max(1, state.current + delta));
  if (next === state.current) return;
  state.current = next;
  state.tab = "learn";
  saveState();
  render();
  window.scrollTo({ top: 0, behavior: "smooth" });
}

function render() {
  const lesson = getLesson();
  $("#lessonSelect").value = String(lesson.id);
  $("#progressLabel").textContent = `Class ${lesson.id} of ${LESSONS.length}`;
  $("#lessonTitle").textContent = lesson.grammar;
  $("#progressFill").style.width = `${Math.round((lesson.id / LESSONS.length) * 100)}%`;
  $("#prevBtn").disabled = lesson.id === 1;
  $("#nextBtn").disabled = lesson.id === LESSONS.length;

  const template = $("#lessonTemplate").content.cloneNode(true);
  template.querySelector('[data-field="classPill"]').textContent = `Class ${lesson.id} · ${lesson.context || "French B1"}`;
  template.querySelector('[data-field="title"]').textContent = lesson.grammar;
  template.querySelector('[data-field="objective"]').textContent = lesson.objective || "Practise this B1 grammar point through useful sentences and active recall.";
  $("#app").innerHTML = "";
  $("#app").appendChild(template);

  $$(".tab").forEach(btn => {
    const isExam = btn.dataset.tab === "exam";
    btn.style.display = isExam && (!lesson.exam || lesson.exam.length === 0) ? "none" : "block";
    btn.classList.toggle("active", btn.dataset.tab === state.tab);
    btn.addEventListener("click", () => {
      state.tab = btn.dataset.tab;
      vibrate();
      renderTab(lesson);
      $$(".tab").forEach(b => b.classList.toggle("active", b.dataset.tab === state.tab));
    });
  });
  renderTab(lesson);
}

function renderTab(lesson) {
  const target = $("#tabContent");
  if (state.tab === "learn") target.innerHTML = renderLearn(lesson);
  if (state.tab === "sentences") target.innerHTML = renderSentences(lesson);
  if (state.tab === "practice") target.innerHTML = renderPractice(lesson);
  if (state.tab === "exam") target.innerHTML = renderExam(lesson);
  bindDynamicActions(target, lesson);
}

